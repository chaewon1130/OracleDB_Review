-- 다중행 함수
SELECT SUM(SAL)
FROM EMP;

SELECT COUNT(*),
    COUNT(COMM)
FROM EMP;

SELECT SUM(ALL SAL),
    SUM(DISTINCT SAL),
    SUM(SAL)
FROM EMP;

SELECT AVG(SAL),
    AVG(DISTINCT SAL)
FROM EMP;

SELECT AVG(COMM),
    AVG(NVL(COMM, 0)),
    SUM(COMM),
    SUM(NVL(COMM, 0))
FROM EMP;

SELECT MAX(HIREDATE),
    MIN(HIREDATE)
FROM EMP;

SELECT HIREDATE
FROM EMP;

SELECT DEPTNO,
    SUM(SAL)
FROM EMP
GROUP BY DEPTNO;

SELECT DEPTNO,
    AVG(SAL)
FROM EMP
WHERE SAL <= 3000
GROUP BY DEPTNO
HAVING AVG(SAL) > 200;

SELECT DEPTNO,
    JOB,
    SUM(SAL)
FROM EMP
GROUP BY DEPTNO, JOB;

SELECT DEPTNO,
    JOB,
    AVG(SAL)
FROM EMP
GROUP BY ROLLUP(DEPTNO, JOB);

SELECT DEPTNO,
    JOB,
    AVG(SAL)
FROM EMP
GROUP BY CUBE(DEPTNO, JOB);

SELECT DEPTNO,
    JOB,
    COUNT(*)
FROM EMP
GROUP BY DEPTNO, ROLLUP(JOB);
SELECT DEPTNO,
    JOB,
    COUNT(*)
FROM EMP
GROUP BY JOB, ROLLUP(DEPTNO);